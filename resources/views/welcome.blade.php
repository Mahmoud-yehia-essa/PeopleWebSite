document.addEventListener('DOMContentLoaded', () => {
    const container = document.createElement('div');
    container.className = 'fixed inset-0 pointer-events-none z-[1] overflow-hidden';
    document.body.insertBefore(container, document.body.firstChild);

    const emojis = ['👑', '👍', '❤️', '💬'];
    
    // Subtle luxury tints using CSS filters
    const filters = [
        'sepia(1) hue-rotate(10deg) saturate(200%)', // Warm Gold
        'sepia(1) hue-rotate(120deg) saturate(100%)', // Emerald
        'grayscale(1) brightness(1.5)' // Soft White/Silver
    ];

    function createParticle() {
        const p = document.createElement('div');
        const emoji = emojis[Math.floor(Math.random() * emojis.length)];
        const filter = filters[Math.floor(Math.random() * filters.length)];
        
        const size = Math.random() * 20 + 12; // 12px to 32px
        const left = Math.random() * 100;
        const duration = Math.random() * 20 + 25; // 25s to 45s for gentle drift
        const opacity = Math.random() * 0.15 + 0.05; // 0.05 to 0.20
        const blur = Math.random() * 4; // 0px to 4px for depth perception
        const drift = (Math.random() - 0.5) * 150; 
        
        p.innerText = emoji;
        p.style.position = 'absolute';
        p.style.left = `${left}%`;
        p.style.bottom = '-50px';
        p.style.fontSize = `${size}px`;
        p.style.filter = `${filter} blur(${blur}px)`;
        p.style.opacity = '0';
        p.style.userSelect = 'none';
        p.style.willChange = 'transform, opacity';
        p.style.fontFamily = 'Cairo, sans-serif';
        
        container.appendChild(p);
        
        const animation = p.animate([
            { transform: `translateY(0) translateX(0) rotate(0deg)`, opacity: 0 },
            { opacity: opacity, offset: 0.1 },
            { opacity: opacity, offset: 0.9 },
            { transform: `translateY(-110vh) translateX(${drift}px) rotate(${drift/2}deg)`, opacity: 0 }
        ], {
            duration: duration * 1000,
            easing: 'linear',
            fill: 'forwards'
        });
        
        animation.onfinish = () => p.remove();
    }

    // Initial background population
    for(let i=0; i<35; i++) {
        setTimeout(createParticle, Math.random() * 12000);
    }
    
    // Continuous subtle spawn
    setInterval(createParticle, 1200);
});
